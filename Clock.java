public class Clock {

	/////////////////////////////////////////////////////////
	//		Clock.java
	//		Represents system clock for a 6502
	//
	//		Michael F. R. Jean
	//		umjeanm@cc.umanitoba.ca
	/////////////////////////////////////////////////////////

	//////////////////////////
	//		Instance variables
	//////////////////////////
	private int clock;

	//////////////////////////
	//		Constructor
	//////////////////////////

	public Clock() {
		clock = 0;
	}

	/////////////////////////
	//		Accessors
	//////////////////////////
	public int getClock() { return clock; }
	public void update(int cycles) { clock += cycles; }

} // Clock